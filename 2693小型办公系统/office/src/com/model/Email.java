package com.model;

public class Email {
     int id;
     String time;
     String context;
     String fsperson;
     String jsperson;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getContext() {
		return context;
	}
	public void setContext(String context) {
		this.context = context;
	}
	public String getFsperson() {
		return fsperson;
	}
	public void setFsperson(String fsperson) {
		this.fsperson = fsperson;
	}
	public String getJsperson() {
		return jsperson;
	}
	public void setJsperson(String jsperson) {
		this.jsperson = jsperson;
	}
    
}
