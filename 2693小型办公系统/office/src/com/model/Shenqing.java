package com.model;

public class Shenqing {
      int id;
      String username;
      String shengqingtype;
      String date;
      String context;
      String status;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getContext() {
		return context;
	}
	public void setContext(String context) {
		this.context = context;
	}
	public String getShengqingtype() {
		return shengqingtype;
	}
	public void setShengqingtype(String shengqingtype) {
		this.shengqingtype = shengqingtype;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
     
}
